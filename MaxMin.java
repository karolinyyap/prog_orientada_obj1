
package maxmin;

public class MaxMin {

    public static void main(String[] args) {
        int a = 20, b = 30;
        
        double c = -5.9, d = -4.5;
        
        //Exemplo de sobrecarga
        System.out.println(Math.max(a,b));
        System.out.println(Math.max(c,d));
    }
    
}
